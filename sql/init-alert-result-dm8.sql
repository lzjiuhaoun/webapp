DECLARE
    CNT INTEGER;
BEGIN
    SELECT COUNT(*) INTO CNT FROM ALL_TABLES WHERE OWNER = 'SCM_RJ_SIMP' AND TABLE_NAME = 'ALERT_RESULT';
    IF CNT > 0 THEN
        EXECUTE IMMEDIATE 'DROP TABLE SCM_RJ_SIMP.ALERT_RESULT CASCADE CONSTRAINTS';
    END IF;
END;
/

CREATE TABLE SCM_RJ_SIMP.ALERT_RESULT (
    ID              INT IDENTITY(1,1) PRIMARY KEY,
    ALERT_RULE_ID   INT NOT NULL,
    RULE_TYPE       TINYINT NOT NULL,
    AUDIT_LOG_IDS   TEXT NOT NULL,
    NORMAL_RULE_ID  INT DEFAULT NULL,
    ALERT_LEVEL     TINYINT NOT NULL,
    TRIGGER_COUNT   INT DEFAULT 1,
    CREATE_TIME     BIGINT NOT NULL
);

COMMENT ON TABLE SCM_RJ_SIMP.ALERT_RESULT IS '告警事件表';
COMMENT ON COLUMN SCM_RJ_SIMP.ALERT_RESULT.ALERT_RULE_ID IS '命中规则 ID';
COMMENT ON COLUMN SCM_RJ_SIMP.ALERT_RESULT.RULE_TYPE IS '规则类型: 0=普通, 1=组合';
COMMENT ON COLUMN SCM_RJ_SIMP.ALERT_RESULT.AUDIT_LOG_IDS IS '审计源数据全局 ID，逗号分隔';
COMMENT ON COLUMN SCM_RJ_SIMP.ALERT_RESULT.NORMAL_RULE_ID IS '组合规则引用的普通规则 ID';
COMMENT ON COLUMN SCM_RJ_SIMP.ALERT_RESULT.ALERT_LEVEL IS '告警等级: 1=红, 2=橙, 3=黄';
COMMENT ON COLUMN SCM_RJ_SIMP.ALERT_RESULT.TRIGGER_COUNT IS '实际触发次数';
COMMENT ON COLUMN SCM_RJ_SIMP.ALERT_RESULT.CREATE_TIME IS '创建时间';

CREATE INDEX IDX_ALERT_RESULT_RULE_ID ON SCM_RJ_SIMP.ALERT_RESULT (ALERT_RULE_ID);
CREATE INDEX IDX_ALERT_RESULT_TIME ON SCM_RJ_SIMP.ALERT_RESULT (CREATE_TIME);
